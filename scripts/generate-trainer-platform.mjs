import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const ROOT = process.cwd();
const SOURCE_PATH = path.join(ROOT, 'data', 'trainer-platform-contracts.json');
const GENERATED_TS_PATH = path.join(ROOT, 'src', 'config', 'generatedTrainerContracts.ts');
const GENERATED_JS_PATH = path.join(ROOT, 'js', 'trainer-platform-contracts.js');

function stableJson(value) {
  return JSON.stringify(value, null, 2);
}

function toTsLiteral(value, indent = 0) {
  const space = ' '.repeat(indent);
  const next = ' '.repeat(indent + 2);
  if (Array.isArray(value)) {
    if (!value.length) return '[]';
    return `[\n${value.map((item) => `${next}${toTsLiteral(item, indent + 2)}`).join(',\n')}\n${space}]`;
  }
  if (value && typeof value === 'object') {
    const entries = Object.entries(value);
    if (!entries.length) return '{}';
    return `{\n${entries.map(([key, entry]) => `${next}${JSON.stringify(key)}: ${toTsLiteral(entry, indent + 2)}`).join(',\n')}\n${space}}`;
  }
  return JSON.stringify(value);
}

function escapeInlineScriptJson(jsonText) {
  return jsonText.replace(/</g, '\\u003c');
}

function buildRuntimeRegistry(contracts) {
  const jsonLiteral = escapeInlineScriptJson(stableJson(contracts));
  return `(function initMetaTrainerPlatformContracts(global) {
    if (!global) return;

    function deepFreeze(value) {
        if (!value || typeof value !== 'object' || Object.isFrozen(value)) return value;
        Object.getOwnPropertyNames(value).forEach(function freezeChild(key) {
            deepFreeze(value[key]);
        });
        return Object.freeze(value);
    }

    var contracts = deepFreeze(${jsonLiteral});
    global.MetaTrainerPlatformContracts = contracts;
    global.getMetaTrainerPlatformContract = function getMetaTrainerPlatformContract(trainerId) {
        return contracts[String(trainerId || '').trim()] || null;
    };
})(typeof globalThis !== 'undefined' ? globalThis : window);
`;
}

function buildGeneratedTs(contracts) {
  return `/* Auto-generated by scripts/generate-trainer-platform.mjs. Do not edit directly. */
import type { TrainerPlatformContractMap } from './trainerContract';

export const TRAINER_PLATFORM_CONTRACTS: TrainerPlatformContractMap = ${toTsLiteral(contracts, 0)};

export default TRAINER_PLATFORM_CONTRACTS;
`;
}

function pickBootConfig(contract) {
  const standalone = contract?.wrapper?.standalone || {};
  const config = {
    trainerId: contract.id,
    manifestKey: standalone.manifestKey
  };

  [
    'bundlePath',
    'buildMetaGlobalKey',
    'assetVersionGlobalKey',
    'css',
    'scripts'
  ].forEach((key) => {
    if (standalone[key] !== undefined) config[key] = standalone[key];
  });

  return config;
}

function buildStandaloneHtml(contract) {
  const bootConfig = JSON.stringify(pickBootConfig(contract), null, 6);
  const standalone = contract?.wrapper?.standalone || {};
  const bodyHtml = standalone.bodyHtml || '';
  const bodyScripts = Array.isArray(standalone.bodyScripts) ? standalone.bodyScripts : [];
  const bodyScriptTags = bodyScripts.map(s => `  <script src="${s}"></script>`).join('\n');
  return `<!doctype html>
<html lang="he" dir="rtl">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <meta http-equiv="Cache-Control" content="no-cache, no-store, must-revalidate">
  <meta http-equiv="Pragma" content="no-cache">
  <meta http-equiv="Expires" content="0">
  <title>${contract.wrapper.pageTitle}</title>
  <link rel="icon" href="data:,">
  <script src="js/trainer-platform-contracts.js"></script>
  <script src="js/trainer-standalone.js"></script>
  <script>
    window.MetaTrainerStandalone.boot(${bootConfig});
  </script>
</head>
<body>${bodyHtml ? '\n' + bodyHtml : ''}${bodyScriptTags ? '\n' + bodyScriptTags : ''}</body>
</html>
`;
}

async function writeIfChanged(filePath, content) {
  let current = '';
  try {
    current = await readFile(filePath, 'utf8');
  } catch (_error) {
    current = '';
  }
  if (current === content) return false;
  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, 'utf8');
  return true;
}

async function run() {
  const raw = await readFile(SOURCE_PATH, 'utf8');
  const parsed = JSON.parse(raw);
  const contracts = parsed?.contracts;
  if (!contracts || typeof contracts !== 'object') {
    throw new Error('trainer platform contracts JSON is missing a "contracts" object');
  }

  const outputs = [
    [GENERATED_TS_PATH, buildGeneratedTs(contracts)],
    [GENERATED_JS_PATH, buildRuntimeRegistry(contracts)]
  ];

  for (const contract of Object.values(contracts)) {
    const htmlFile = contract?.wrapper?.standalone?.htmlFile;
    if (!htmlFile) continue;
    outputs.push([path.join(ROOT, htmlFile), buildStandaloneHtml(contract)]);
  }

  for (const [filePath, content] of outputs) {
    const changed = await writeIfChanged(filePath, content);
    const label = path.relative(ROOT, filePath);
    console.log(`${changed ? 'wrote' : 'kept'} ${label}`);
  }
}

run().catch((error) => {
  console.error('generate-trainer-platform failed:', error.message);
  process.exit(1);
});
