function questionForCEQ(tokenText) {
  return `מה נחשב אצלך "${tokenText}"? תן 2–3 דוגמאות/סימנים.`;
}

function questionForCause(tokenText, mode) {
  if (mode === 'EFFECTS_OF_TOKEN') return `מה "${tokenText}" מאפשר לך?`;
  return `מה גורם/מאפשר לך "${tokenText}"?`;
}

function questionForAssumptions(tokenText) {
  return `איזה דברים משתמעים/מונחים כאן כשאתה אומר "${tokenText}"?`;
}

function reflectionForCEQ(tokenText) {
  return `אז "${tokenText}" אצלך יכול להיות = {0} + {1} + {2}. (אילוסטרציה בלבד — לא אמת.)`;
}

function reflectionForCause(tokenText, mode) {
  if (mode === 'EFFECTS_OF_TOKEN') {
    return `"${tokenText}" יכול לאפשר: {0} + {1}. (אילוסטרציה בלבד — לא אמת.)`;
  }
  return `מבנה סיבתי אפשרי: {0} + {1} ← "${tokenText}". (אילוסטרציה בלבד — לא אמת.)`;
}

function reflectionForAssumptions() {
  return 'ייתכן שהמשפט נשען על הנחות כגון: {0}; {1}; {2}. (אילוסטרציה בלבד — לא אמת.)';
}

function makeScenario(seed) {
  const tokenText = seed.tokenText;
  const idx = seed.clientText.indexOf(tokenText);
  if (idx < 0) throw new Error(`Token "${tokenText}" not found in scenario ${seed.id}`);

  const causeMode = seed.cause?.mode || 'CAUSES_OF_TOKEN';
  const draggable = {
    id: `tok_${seed.id.toLowerCase()}`,
    type: tokenText.includes(' ') ? 'phrase' : 'word',
    text: tokenText,
    start: idx,
    end: idx + tokenText.length,
    allowed_templates: ['CEQ', 'CAUSE', 'ASSUMPTIONS1']
  };

  return {
    scenario_id: seed.id,
    language: 'he',
    client_text: seed.clientText,
    draggables: [draggable],
    template_payloads: {
      [draggable.id]: {
        CEQ: {
          question: questionForCEQ(tokenText),
          sets: seed.ceq,
          reflection_template: reflectionForCEQ(tokenText)
        },
        CAUSE: {
          mode: causeMode,
          question: questionForCause(tokenText, causeMode),
          sets: seed.cause.sets,
          reflection_template: reflectionForCause(tokenText, causeMode)
        },
        ASSUMPTIONS1: {
          question: questionForAssumptions(tokenText),
          sets: seed.assumptions,
          reflection_template: reflectionForAssumptions()
        }
      }
    }
  };
}

async function main() {
  const scenarios = SCENARIO_SEEDS.map(makeScenario);
  const out = {
    version: '0.2.0',
    language: 'he',
    templatesMeta: TEMPLATE_META,
    scenarios
  };
  await fs.writeFile(OUT_FILE, `${JSON.stringify(out, null, 2)}\n`, 'utf8');
  console.log(`WROTE ${OUT_FILE} (${scenarios.length} scenarios)`);
}

main().catch((error) => {
  console.error('FAIL generate-iceberg-templates-data:', error);
  process.exit(1);
});
