import { build } from 'esbuild';
import path from 'node:path';
import process from 'node:process';

const ROOT = process.cwd();
const ENTRY = path.join(ROOT, 'src', 'iceberg-templates-standalone.tsx');
const OUTFILE = path.join(ROOT, 'js', 'iceberg-templates-trainer.bundle.js');

async function run() {
  await build({
    entryPoints: [ENTRY],
    outfile: OUTFILE,
    bundle: true,
    format: 'iife',
    platform: 'browser',
    target: ['es2020'],
    sourcemap: false,
    minify: true,
    jsx: 'automatic',
    loader: {
      '.tsx': 'tsx',
      '.ts': 'ts'
    },
    banner: {
      js: '/* Auto-generated by scripts/build-iceberg-templates.mjs. Do not edit directly. */'
    }
  });
  console.log(`Built Iceberg Templates standalone bundle -> ${path.relative(ROOT, OUTFILE)}`);
}

run().catch((error) => {
  console.error('build-iceberg-templates failed:', error.message);
  process.exit(1);
});

