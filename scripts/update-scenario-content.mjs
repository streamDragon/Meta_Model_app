function mergeOption(target, patch) {
  if (!target || !patch) return;
  Object.assign(target, patch);
}

function applyScenarioPatch(scenario, patch) {
  Object.entries(patch).forEach(([key, value]) => {
    if (key === 'responseSet') return;
    scenario[key] = value;
  });

  if (!patch.responseSet) return;
  if (Array.isArray(patch.responseSet.red)) {
    for (const optionPatch of patch.responseSet.red) {
      const target = scenario.responseSet.red.find((item) => item.id === optionPatch.id);
      if (target) mergeOption(target, optionPatch);
    }
  }
  if (patch.responseSet.green) {
    mergeOption(scenario.responseSet.green, patch.responseSet.green);
  }
}

const raw = await readFile(DATA_PATH, 'utf8');
const data = JSON.parse(raw);

for (const scenario of data.scenarios) {
  const defaults = domainDefaults[scenario.domain];
  if (defaults) {
    scenario.learningFocus = defaults.learningFocus;
    scenario.supportPrompt = defaults.supportPrompt;
  }
  const patch = overrides[scenario.scenarioId];
  if (patch) applyScenarioPatch(scenario, patch);
}

